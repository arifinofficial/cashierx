    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required|min:2',
            'value' => 'required|integer',
            'status' => 'required'
        ]);

        $model = Discount::create($request->all());

        if ($model) {
            return redirect()->back()->with(['success' => 'Discount: <strong>' . $model->name . '</strong> Ditambahkan']);
        }

        return redirect()->back()->with(['error' => '<strong> Error! </strong>']);
    }

